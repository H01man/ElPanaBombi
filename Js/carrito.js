totalcom = 0
array = [ ];

var cantidad = document.getElementById('cantidad1');

cantidad.addEventListener('keyup' , (event) => {
    if(cantidad.value == null || cantidad.value == 0){
        totalcom = totalcom - totalpag
        document.getElementById('tcomp').innerHTML = 0
    }else{
        total = cantidad.value * 5000
        document.getElementById('total1').innerHTML = total

        iva = (total * 19)/100
        totalpag = total + iva
        document.getElementById('toti1').innerHTML = totalpag
        totalcom = totalcom + totalpag
        tot = array.push(totalcom)
        document.getElementById('tcomp').innerHTML = totalcom
    }
})

